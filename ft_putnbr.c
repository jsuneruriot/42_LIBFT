#include "libft.h"

void	ft_putnbr(int n)
{
	if (n >= 0 && n < 10)
		ft_putchar(n + 48);
	else if (n < 0)
	{
		ft_putchar('-');
		if (n < -9)
		{
			ft_putnbr(n / 10 * -1);
			ft_putnbr(n % 10 * -1);
		}
		else
			ft_putnbr(n % 10 * -1);
	}
	else
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
