#include "libft.h"

char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	tmp;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	while (i - 1 > j)
	{
		tmp = str[i - 1];
		str[i - 1] = str[j];
		str[j] = tmp;
		j++;
		i--;
	}
	return (str);
}
